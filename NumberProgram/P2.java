package NumberProgram;

import java.util.Scanner;

class P2{
	public static void main(String[] args){
		Scanner sc = new Scanner(System.in);
		
		System.out.print("Enter number:");
		int num = sc.nextInt();
		
		for(int i=1; i<=num; i++){
			for(int j=1; j<=j; j++){
				System.out.println(i + " " + j);
				break;
			}
			System.out.println();
		}
				
	}
} 
