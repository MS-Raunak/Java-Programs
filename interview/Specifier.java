package interview;/*
1.        print single backslash (\)
2.        print \"
3.        print java "is" java

Basically backslash(\) is used to escape the meaning of
next character.
 */

public class Specifier {
    public static void main(String[] args) {
        System.out.println("\\");  // \
        System.out.println("\\\""); // \*
        System.out.println("java \"is\" java "); // java "is" java
    }
}

